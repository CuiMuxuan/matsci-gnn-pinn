def build_gate(
    *,
    phase97_gate: dict[str, Any],
    unlock_rows: list[dict[str, Any]],
) -> dict[str, Any]:
    ready_rows = [row for row in unlock_rows if row["phase99_local_smoke_allowed"]]
    ambench_ready = [
        row
        for row in ready_rows
        if row["dataset_id"] not in {"phase96_pfhub_style_heat_source_v1", "pfhub_style_local"}
    ]
    if not _phase97_fixed_signal_available(phase97_gate):
        status = "blocked_no_phase97_positive_mechanism"
        next_action = "close fixed-kernel transfer; no positive mechanism is available"
        phase99_allowed = False
    elif ready_rows:
        status = "registered_surrogate_unlocked_no_a100"
        next_action = "enter Phase 99 local baseline-first smoke on the generated registered surrogate"
        phase99_allowed = True
    else:
        status = "blocked_no_unlock_candidate"
        next_action = "resolve pad registration or provide an external registered data card"
        phase99_allowed = False
    return {
        "status": status,
        "source_phase97_status": phase97_gate.get("status"),
        "phase99_local_smoke_allowed": phase99_allowed,
        "a100_training_allowed_now": False,
        "a100_80gb_request_now": False,
        "submission_ready": False,
        "unlock_rows": len(unlock_rows),
        "phase99_candidate_rows": len(ready_rows),
        "ambench_phase99_candidate_rows": len(ambench_ready),
        "blocked_rows": sum(1 for row in unlock_rows if str(row["status"]).startswith("blocked")),
        "preferred_phase99_candidate": ready_rows[0]["candidate_id"] if ready_rows else "none",
        "am_bench_transfer_unlocked": bool(ambench_ready),
        "next_action": next_action,
        "required_before_a100_training": [
            "Phase 99 local baseline-first smoke",
            "non-worse global/hot/gradient metrics",
            "strong baseline comparison",
            "fixed split and no-test-leakage selection",
            "server validation from a pushed commit",
        ],
    }


def _markdown_table(rows: list[dict[str, Any]], columns: list[tuple[str, str]]) -> str:
    if not rows:
        return "_No rows._"
    header = "| " + " | ".join(label for _, label in columns) + " |"
    sep = "| " + " | ".join("---" for _ in columns) + " |"
    body = [
        "| " + " | ".join(_csv_value(row.get(key)).replace("\n", " ") for key, _ in columns) + " |"
        for row in rows
    ]
    return "\n".join([header, sep, *body])


def build_markdown(
    gate: dict[str, Any],
    unlock_rows: list[dict[str, Any]],
    protocol_rows: list[dict[str, Any]],
) -> str:
    return "\n".join(
        [
            "# Phase 98 Registered Transfer Target Unlock Gate",
            "",
            "## Gate Decision",
            "",
            f"Status: `{gate['status']}`.",
            f"Phase 99 local smoke allowed: `{str(gate['phase99_local_smoke_allowed']).lower()}`.",
            f"AM-Bench transfer unlocked: `{str(gate['am_bench_transfer_unlocked']).lower()}`.",
            f"A100 training allowed now: `{str(gate['a100_training_allowed_now']).lower()}`.",
            f"A100-SXM4-80GB request now: `{str(gate['a100_80gb_request_now']).lower()}`.",
            "",
            "Phase 98 can unlock local smoke for a registered target or generated surrogate only. It is not an A100 training gate.",
            "",
            "## Unlock Candidates",
            "",
            _markdown_table(
                unlock_rows,
                [
                    ("candidate_id", "Candidate"),
                    ("dataset_id", "Dataset"),
                    ("registration_status", "Registration"),
                    ("status", "Status"),
                    ("phase99_local_smoke_allowed", "Phase 99"),
                    ("next_action", "Next action"),
                ],
            ),
            "",
            "## Protocol",
            "",
            _markdown_table(
                protocol_rows,
                [
                    ("protocol_id", "Protocol"),
                    ("component", "Component"),
                    ("pass_condition", "Pass"),
                    ("stop_condition", "Stop"),
                ],
            ),
            "",
            "## Next Action",
            "",
            gate["next_action"],
            "",
        ]
    )


def build_package(
    root: Path,
    output_dir: Path,
    paths: dict[str, Path] | None = None,
) -> dict[str, Any]:
    resolved = _default_paths(root)
    if paths:
        resolved.update(paths)

    phase96_target = _read_json(resolved["phase96_target_manifest"])
    phase97_gate = _read_json(resolved["phase97_gate"])
    transfer_rows = _read_csv(resolved["phase97_transfer_routes"])

    surrogate_card = build_registered_surrogate_card(phase96_target)
    unlock_rows = build_unlock_rows(
        phase97_gate=phase97_gate,
        transfer_rows=transfer_rows,
        surrogate_card=surrogate_card,
    )
    protocol_rows = build_protocol_rows()
    gate = build_gate(phase97_gate=phase97_gate, unlock_rows=unlock_rows)

    output_dir.mkdir(parents=True, exist_ok=True)
    unlock_path = output_dir / "phase98_unlock_candidate_table.csv"
    protocol_path = output_dir / "phase98_unlock_protocol.csv"
    surrogate_card_path = output_dir / "phase98_registered_surrogate_data_card.json"
    gate_path = output_dir / "phase98_registered_target_unlock_gate.json"
    markdown_path = output_dir / "phase98_registered_target_unlock_gate.md"
    manifest_path = output_dir / "phase98_registered_target_unlock_gate_manifest.json"

    _write_csv(unlock_path, unlock_rows, UNLOCK_FIELDS)
    _write_csv(protocol_path, protocol_rows, PROTOCOL_FIELDS)
    _write_json(surrogate_card_path, surrogate_card)
    _write_json(gate_path, gate)
    markdown_path.write_text(build_markdown(gate, unlock_rows, protocol_rows), encoding="utf-8")

    manifest = {
        "phase": 98,
        "objective": "registered_transfer_target_unlock_gate",
        "inputs": {key: _display_path(path, root) for key, path in sorted(resolved.items())},
        "outputs": {
            "unlock_candidate_table": _display_path(unlock_path, root),
            "unlock_protocol": _display_path(protocol_path, root),
            "registered_surrogate_data_card": _display_path(surrogate_card_path, root),
            "gate_json": _display_path(gate_path, root),
            "markdown": _display_path(markdown_path, root),
            "manifest": _display_path(manifest_path, root),
        },
        "counts": {
            "unlock_rows": len(unlock_rows),
            "protocol_rows": len(protocol_rows),
            "phase99_candidate_rows": gate["phase99_candidate_rows"],
            "ambench_phase99_candidate_rows": gate["ambench_phase99_candidate_rows"],
            "blocked_rows": gate["blocked_rows"],
        },
        "gate": gate,
        "phase97_gate": {
            "status": phase97_gate.get("status"),
            "positive_mechanisms": phase97_gate.get("positive_mechanisms"),
            "phase98_local_smoke_allowed": phase97_gate.get("phase98_local_smoke_allowed"),
        },
        "source_target": {
            "target_id": phase96_target.get("target_id"),
            "target_type": phase96_target.get("target_type"),
        },
    }
    _write_json(manifest_path, manifest)
    return manifest


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--root", type=Path, default=Path("."))
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("docs/results/phase98_registered_target_unlock_gate"),
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    root = args.root.resolve()
    output_dir = args.output_dir
    if not output_dir.is_absolute():
        output_dir = root / output_dir
    manifest = build_package(root=root, output_dir=output_dir)
    print(json.dumps(manifest, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
