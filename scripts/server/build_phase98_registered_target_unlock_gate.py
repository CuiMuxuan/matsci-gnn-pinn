def _unlock_row_from_phase97_route(row: dict[str, str]) -> dict[str, Any]:
    route_id = row["route_id"]
    status = row.get("status", "")
    is_open = _truthy(row.get("phase98_local_smoke_allowed"))
    if is_open:
        unlock_status = "phase99_local_smoke_ready_no_a100"
        next_action = "enter Phase 99 local baseline-first smoke for registered route"
    elif status == "blocked_missing_registration":
        unlock_status = "blocked_registration_evidence_required"
        next_action = "provide camera-to-galvo or equivalent coordinate registration before local smoke"
    elif status == "blocked_no_data_card":
        unlock_status = "blocked_source_manifest_data_card_required"
        next_action = "provide public source manifest, registration story, split plan, and baseline plan"
    elif status == "synthetic_appendix_only":
        unlock_status = "handled_by_generated_surrogate_candidate"
        next_action = "use generated registered surrogate row for local mechanism smoke"
    elif status.startswith("blocked"):
        unlock_status = status
        next_action = row.get("next_action") or "resolve transfer blocker before local smoke"
    else:
        unlock_status = "diagnostic_only"
        next_action = row.get("next_action") or "keep as diagnostic evidence"
    return {
        "candidate_id": f"phase98_unlock_{route_id}",
        "source_route_id": route_id,
        "candidate_family": row.get("route_family", ""),
        "dataset_id": row.get("dataset_id", ""),
        "target_type": row.get("transfer_target", ""),
        "source_manifest_status": "missing_or_inherited_from_blocked_route",
        "registration_status": row.get("registration_status", ""),
        "split_plan_status": row.get("split_status", ""),
        "baseline_plan_status": row.get("baseline_status", ""),
        "licensing_status": "inherited_or_missing",
        "paper_use": row.get("status", ""),
        "phase99_local_smoke_allowed": is_open,
        "a100_training_allowed": False,
        "status": unlock_status,
        "priority": int(row.get("priority") or 99) + 10,
        "next_action": next_action,
        "evidence": row.get("evidence", ""),
    }


def build_unlock_rows(
    *,
    phase97_gate: dict[str, Any],
    transfer_rows: list[dict[str, str]],
    surrogate_card: dict[str, Any],
) -> list[dict[str, Any]]:
    fixed_signal_available = _phase97_fixed_signal_available(phase97_gate)
    rows = [_surrogate_unlock_row(surrogate_card, fixed_signal_available)]
    rows.extend(_unlock_row_from_phase97_route(row) for row in transfer_rows)
    return sorted(rows, key=lambda row: int(row["priority"]))


