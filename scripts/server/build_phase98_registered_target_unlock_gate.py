#!/usr/bin/env python3
"""Build the Phase 98 registered transfer-target unlock gate.

Phase 98 tries to turn the Phase 97 transfer-design blocker into concrete data
work. It can unlock a local baseline-first smoke target when a route has a
reproducible source manifest, a registration story, fixed splits, and baseline
comparators. It never opens A100 training.
"""

